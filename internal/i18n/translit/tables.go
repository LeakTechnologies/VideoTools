package translit

func initTables() {
	buildS2R()
	buildR2S()
}

func buildS2R() {
	s2r = map[rune]string{}
	for _, e := range s2rTab {
		s2r[rune(e.syl)] = e.rom
	}
	s2rCompound = map[string]string{}
	for _, e := range s2rCompoundTab {
		s2rCompound[string([]rune{e.final, e.next})] = e.rom
	}
}

func buildR2S() {
	r2s = map[string]string{}
	r2sMaxKey = 0
	for _, e := range r2sTab {
		r2s[e.rom] = string(e.syl)
		if len(e.rom) > r2sMaxKey {
			r2sMaxKey = len(e.rom)
		}
	}
}

